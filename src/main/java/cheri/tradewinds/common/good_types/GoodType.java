package cheri.tradewinds.common.good_types;

public class GoodType {
	public String id;
	public String name;
	public String icon;
	public String desc;

	public GoodType(String id, String name, String icon, String desc) {
		this.id = id;
		this.name = name;
		this.icon = icon;
		this.desc = desc;
	}
}
